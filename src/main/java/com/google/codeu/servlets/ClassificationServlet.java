/**
 * Responds with a hard-coded message for testing purposes.
 */
@WebServlet("/content-classification")
public class ClassificationServlet extends HttpServlet{
    
    private Datastore datastore;
    @Override
    
    public void init() {
    datastore = new Datastore();
    }
    /**
    * Responds with site statistics in JSON.
    */
    
    @Override
    public void doGet(HttpServletRequest request, HttpServletResponse response)
        throws IOException {

        response.setContentType("application/json");

        String contentConfidence = datastore.getContentConfidences();

        JsonObject jsonObject = new JsonObject();
        jsonObject.addProperty("contentConfidence", contentConfidence);
        response.getOutputStream().println(jsonObject.toString());
    }
}
